    private void popuniTabelu() {
        //Lista grupa treba da se napravi - preko kontrolera
        //Kad dobijemo listu grupa, tableModel treba da zna za tu listu - treba nam konstruktor(parametarski sa listom grupa) i set i get metoda u TM

        ServerskiOdgovor so = Kontroler.getInstance().vratiListuGrupa();
        ArrayList<Grupa> listaGrupa = (ArrayList<Grupa>) so.getOdgovor();

        TableModelGrupa tmg = new TableModelGrupa(listaGrupa);
        jTablePrikaz.setModel(tmg);

    }

    private void pretraga() {
        ServerskiOdgovor so = Kontroler.getInstance().pretraziGrupe();
        ArrayList<Grupa> listaGrupa = (ArrayList<Grupa>) so.getOdgovor();
        ArrayList<Grupa> prikaz = new ArrayList<>();

        for (Grupa grupa : listaGrupa) {
            if (ulogovanTrener.getTrenerID().equals(grupa.getTrener().getTrenerID())) {
                prikaz.add(grupa);
            }
        }

        TableModelGrupa tmg = new TableModelGrupa(prikaz);
        jTablePrikaz.setModel(tmg);
    }

