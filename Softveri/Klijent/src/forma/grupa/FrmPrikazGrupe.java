    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int red = jTablePrikaz.getSelectedRow();

        if (red != -1) {

            TableModelGrupa tmg = (TableModelGrupa) jTablePrikaz.getModel();
            Grupa grupa = tmg.uzmiGrupu(red);
            int brojClanovaGrupe = grupa.getBrojClanova();

            if (ulogovanTrener.getTrenerID().equals(grupa.getTrener().getTrenerID())) {

                if (brojClanovaGrupe == 0) {

                    Long grupaID = (Long) jTablePrikaz.getValueAt(red, 0);
                    tmg.obrisiGrupu(red);

                    Grupa obrisana = new Grupa();
                    obrisana.setGrupaID(grupaID);
                    ServerskiOdgovor so = Kontroler.getInstance().obrisiGrupu(obrisana);

                    if (so.isUspesnost()) {
                        JOptionPane.showMessageDialog(this, "Sistem je obrisao izabranu grupu!!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Sistem nije obrisao izabranu grupu!!");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Trener može obrisati samo grupu koja nema članove!!");
                    return;
                }

            } else {
                JOptionPane.showMessageDialog(this, "Trener može obrisati samo svoju grupu!!");
                return;
            }

        } else {
            JOptionPane.showMessageDialog(this, "Označite grupu za brisanje!!");
            return;
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

