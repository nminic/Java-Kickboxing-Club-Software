    private void jButtonUnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUnosActionPerformed
        String dat = jTextFieldDatum.getText();
        String opis = jTextAreaOpis.getText();
        
        if(dat.isEmpty()){
            JOptionPane.showMessageDialog(this, "Morate popuniti polje za datum treninga!!");
            return;
        }
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
        try {
            Date datumVreme = sdf.parse(dat);
            
            if(datumVreme.before(new Date())){
                JOptionPane.showMessageDialog(this, "Novi trening je moguƒáe kreirati za dana≈°nji datum ili naredni period!!");
                return;
            }
            
            Grupa grupa = (Grupa) jComboBoxGrupa.getSelectedItem();
            Trener trener = ulogovanTrener;
            
            if(grupa == null){
                JOptionPane.showMessageDialog(this, "Trener ne trenira nijednu grupu, prvo kreirajte novu grupu!!");
                return;
            }
            
            Trening trening = new Trening(-1L, datumVreme, opis, trener, grupa);
            ServerskiOdgovor so = Kontroler.getInstance().zapamtiTrening(trening);
            JOptionPane.showMessageDialog(this, so.getPoruka());
            
            boolean uspesnost = so.isUspesnost();
            if(uspesnost){
                 JOptionPane.showMessageDialog(this, "Sistem je uspe≈°no saƒçuvao novi trening!!");
            }else{
                 JOptionPane.showMessageDialog(this, "Neuspe≈°no ƒçuvanje novog treninga!!");
            }
            
            resetPolja();
            
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Pogre≈°an format datuma!!");
            Logger.getLogger(FrmUnosTreninga.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        
        
    }//GEN-LAST:event_jButtonUnosActionPerformed

