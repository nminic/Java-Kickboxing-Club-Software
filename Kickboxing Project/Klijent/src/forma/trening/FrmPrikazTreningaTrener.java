    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int red = jTablePretraga.getSelectedRow();

        if (red != -1) {

            TableModelTrening tmt = (TableModelTrening) jTablePretraga.getModel();

            Trening trening = tmt.uzmiTrening(red);

            if (ulogovanTrener.getTrenerID().equals(trening.getTrener().getTrenerID())) {

                Long treningID = (Long) jTablePretraga.getValueAt(red, 0);
                
                tmt.obrisiTrening(red);

                Trening obrisan= new Trening();
                obrisan.setTreningID(treningID);
                
                ServerskiOdgovor so = Kontroler.getInstance().obrisiTrening(obrisan);
                so.getPoruka();

                if (so.isUspesnost()) {
                    JOptionPane.showMessageDialog(this, "Sistem je obrisao izabrani trening!!");
                } else {
                    JOptionPane.showMessageDialog(this, "Sistem nije obrisao izabrani trening!!");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Trener može obrisati samo treninge svojih članova!!");
                return;
            }

        } else {
            JOptionPane.showMessageDialog(this, "Odaberite trening koji želite da obrišete!!");
            return;
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed


