    public void popuniTabelu() {
        ServerskiOdgovor so = Kontroler.getInstance().vratiListuTreninga();
        ArrayList<Trening> listaTreninga = (ArrayList<Trening>) so.getOdgovor();
        ArrayList<Trening> prikaz = new ArrayList<>();

        for (Trening trening : listaTreninga) {
            if (ulogovanTrener.getTrenerID().equals(trening.getTrener().getTrenerID())) {
                prikaz.add(trening);
            }
        }

        TableModelTrening tmt = new TableModelTrening(prikaz);
        jTablePretraga.setModel(tmt);
    }

    private void pretrazi() {
        Date datum = jDateChooserPretraga.getDate();
        ServerskiOdgovor so = Kontroler.getInstance().pretraziTreninge();
        ArrayList<Trening> listaTreninga = (ArrayList<Trening>) so.getOdgovor();
        ArrayList<Trening> prikaz = new ArrayList<>();

        for (Trening trening : listaTreninga) {
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
                Date da = trening.getDatumVreme();
                String dat = sdf.format(da);
                Date datu = sdf.parse(dat);

                if (ulogovanTrener.getTrenerID().equals(trening.getTrener().getTrenerID())) {
                    if (datu.equals(datum)) {
                        prikaz.add(trening);
                    }
                }

            } catch (ParseException ex) {
                Logger.getLogger(FrmPrikazTreningaClan.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Greška sa formatom datuma!!");
            }
        }

        TableModelTrening tmt = (TableModelTrening) jTablePretraga.getModel();
        tmt.setListaTreninga(prikaz);
    }

    private void popuniComboGrupa() {
        jComboBoxGrupa.removeAllItems(); //uklanjanje default vrednosti

        ServerskiOdgovor so = Kontroler.getInstance().vratiListuGrupa();
        ArrayList<Grupa> listaGrupa = (ArrayList<Grupa>) so.getOdgovor();

        for (Grupa g : listaGrupa) {

            if (g.getTrener().getTrenerID().equals(ulogovanTrener.getTrenerID())) {
                jComboBoxGrupa.addItem(g);
            }

        }

    }

    private void pretraziPoGrupi() {

        Grupa grupa = (Grupa) jComboBoxGrupa.getSelectedItem();
        
         if(grupa == null){
            JOptionPane.showMessageDialog(this, "Trener ne trenira nijednu grupu!! Napravi novu grupu!!");
            return;
        }
        ServerskiOdgovor so = Kontroler.getInstance().pretraziTreninge();
        ArrayList<Trening> listaTreninga = (ArrayList<Trening>) so.getOdgovor();
        ArrayList<Trening> prikaz = new ArrayList<>();

        for (Trening trening : listaTreninga) {

            if (ulogovanTrener.getTrenerID().equals(trening.getTrener().getTrenerID())) {
                if(grupa.getGrupaID().equals(trening.getGrupa().getGrupaID())){
                prikaz.add(trening);
                }
            }

        }

        TableModelTrening tmt = (TableModelTrening) jTablePretraga.getModel();
        tmt.setListaTreninga(prikaz);
    }

    Trening uzmiTrening() {
        
        int red = jTablePretraga.getSelectedRow();
        Trening trening;
        if(red != -1){
        
        TableModelTrening tmt = (TableModelTrening) jTablePretraga.getModel();
        trening = tmt.uzmiTrening(red);
        
    } else{
            JOptionPane.showMessageDialog(this, "Nije izabran nijedan trening! Izabreri trening za koji želiš uvid u prisustva!!");
            trening = null;
        }
        return trening;
    }
